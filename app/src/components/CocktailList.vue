<template>
  <div class="cocktail">
    <section>
      <Cocktail
        v-for="cocktail in cocktails"
        :key="cocktail.name"
        :cocktail="cocktail"
        />
    </section>

    <section>
      <AddCocktail :onAdd="handleAdd"/>
    </section>
  </div>
</template>

<script>
import Cocktail from './Cocktail';
import AddCocktail from './AddCocktail';
import { getCocktails, addCocktail } from '../services/api';

export default {

  data() {
    return {
      cocktails: null
    };
  },

  created() {
    getCocktails()
      .then(cocktails => {
        this.cocktails = cocktails;
      });
  },

  methods: {
    handleAdd(cocktail) {
      return addCocktail(cocktail)
        .then(saved => {
          this.cocktails.push(saved);
        });
    }
  },

  components: {
    Cocktail,
    AddCocktail
  }
};
</script>

<style>

.cocktail {
  display: flex;
  justify-content: space-evenly;
}

</style>
